        public static DataTable GetAllUsers()
        {
            DataTable dtPeople = new DataTable();

            SqlConnection connection = new SqlConnection(clsDataAccessSettings.ConnectionString);

            string query = @"select Users.UserID, Users.PersonID, FullName = People.FirstName + ' ' +
                             People.SecondName + case when ThirdName is null then ' ' else ' ' +
                             People.ThirdName + ' ' end + People.LastName, Users.UserName ,Users.IsActive
                             from Users
                             inner join People on People.PersonID = Users.PersonID";

            SqlCommand command = new SqlCommand(query, connection);

            try
            {
                connection.Open();

                SqlDataReader reader = command.ExecuteReader();

                if (reader.HasRows)
                {
                    dtPeople.Load(reader);
                }

                reader.Close();
            }
            catch (Exception ex)
            {

            }
            finally
            {
                connection.Close();
            }

            return dtPeople;
        }

